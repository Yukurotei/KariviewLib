package it.yuruni.kariview.client.data.elements;

public class GuiElementData {
    private String id;
    private String texture;
    private double posX;
    private double posY;
    private int width;
    private int height;

    public String getId() {
        return id;
    }

    public String getTexture() {
        return texture;
    }

    public double getPosX() {
        return posX;
    }

    public double getPosY() {
        return posY;
    }

    public int getWidth() {
        return width;
    }

    public int getHeight() {
        return height;
    }
}
